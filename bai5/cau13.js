
// - Tìm hiểu thêm một số hàm toán học sau:
//     - round() => Làm tròn lên hoặc xuống.
//     - ceil() => Làm tròn lên.
//     - floor() => Làm tròn xuống.
//     - min() => Lấy số nhỏ nhất.
//     - max() => Lấy số lớn nhất.
//     - pow() => Lũy thừa.
//     - random() => Tạo số thập phân ngẫu nhiên từ 0 đến 1.
//     - trunc() => Xóa số thập phân.

console.log(Math.round(99.2)); //99

console.log(Math.round(99.5)); //100

console.log(Math.ceil(99.2)); //100

console.log(Math.floor(99.9)); //99

console.log(Math.min(10, 20, 100, -100, 90)); //-100

console.log(Math.max(10, 20, 100, -100, 90)); //100

console.log(Math.pow(2, 4)); //2**4 = 16

console.log(Math.random()); //số ngẫu nhiên từ 0-1

console.log(Math.trunc(99.5)); //99 xóa số thập phân