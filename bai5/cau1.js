var x = 5;
var y = 3;

// addition
console.log("x + y = ", x + y); //5+3=8

// subtraction
console.log("x - y = ", x - y); // 5-3=2

// multiplication
console.log("x * y = ", x * y); //5*3=15

// division
console.log("x / y = ", x / y); //5/3=1.666666...7

// remainder
console.log("x % y = ", x % y); //5%3=2

// increment
console.log("++x = ", ++x); //++x=6
console.log("x++ = ", x++); //x++=6
console.log("x = ", x); //x=7

// decrement
console.log("--x = ", --x); //--x=6
console.log("x-- = ", x--); //x--=6
console.log("x = ", x); //x=5

//exponentiation
console.log("x ** y =", x ** y); //x**y=125
